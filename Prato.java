public class Prato {
    public String nome_do_prato;
    public String valor;




    public Prato(String nome_do_prato, String valor) {
        this.nome_do_prato = nome_do_prato;
        this.valor = valor;
    }

    public String getNome_do_prato() {
        return nome_do_prato;
    }

    public void setNome_do_prato(String nome_do_prato) {
        this.nome_do_prato = nome_do_prato;
    }
    public String getValor() {
        return valor;
    }
    public void setValor(String valor) {
        this.valor = valor;
    }



    

}